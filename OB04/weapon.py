"""
Модуль с абстрактным классом оружия и его реализациями.
Демонстрирует применение принципа Open/Closed Principle.
"""
from abc import ABC, abstractmethod


class Weapon(ABC):
    """Абстрактный базовый класс для всех типов оружия."""
    
    @abstractmethod
    def attack(self) -> str:
        """
        Абстрактный метод атаки.
        
        Returns:
            str: Описание атаки
        """
        pass


class Sword(Weapon):
    """Класс меча - конкретная реализация оружия."""
    
    def attack(self) -> str:
        """Атака мечом."""
        return "Боец наносит удар мечом."


class Bow(Weapon):
    """Класс лука - конкретная реализация оружия."""
    
    def attack(self) -> str:
        """Атака из лука."""
        return "Боец наносит удар из лука."


class Axe(Weapon):
    """Класс топора - дополнительная реализация для демонстрации расширяемости."""
    
    def attack(self) -> str:
        """Атака топором."""
        return "Боец наносит мощный удар топором."


class Spear(Weapon):
    """Класс копья - еще одна реализация для демонстрации расширяемости."""
    
    def attack(self) -> str:
        """Атака копьем."""
        return "Боец наносит колющий удар копьем."
